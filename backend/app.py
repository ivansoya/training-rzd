@app.get("/api/datasets")
def list_datasets():
    meta = load_aug_meta()
    items = []
    for name in sorted(os.listdir(UPLOADED_DIR)):
        full = os.path.join(UPLOADED_DIR, name)
        if os.path.isdir(full):
            items.append({"name": name, "kind": "uploaded", "images": count_images(full)})
    for name in sorted(os.listdir(AUGMENTED_DIR)):
        full = os.path.join(AUGMENTED_DIR, name)
        if os.path.isdir(full):
            m = meta.get(name, {})
            items.append({
                "name": name,
                "kind": "augmented",
                "images": count_images(full),
                "display_name": m.get("display_name", name),
                "source": m.get("source"),
                "config_names": [s.get("name") for s in m.get("configs", [])],
            })
    return jsonify(items)


@app.get("/api/datasets/<kind>/<name>")
def get_dataset(kind, name):
    root = kind_dir(kind)
    if root is None:
        return jsonify({"error": "unknown kind"}), 404
    name = safe_name(name)
    path = os.path.join(root, name)
    if not os.path.isdir(path):
        return jsonify({"error": "dataset not found"}), 404
    stats = get_cached_stats(path)
    if kind == "augmented":
        stats["meta"] = load_aug_meta().get(name)
    return jsonify(stats)


def _run_upload_job(job_id, tmp_path, dest):
    """Validate and extract an uploaded archive, reporting progress to the job."""
    try:
        jobs.update(job_id, message="Проверка архива")
        try:
            zf = zipfile.ZipFile(tmp_path)
        except zipfile.BadZipFile:
            jobs.update(job_id, status="error", error="file is not a valid zip archive")
            return
        with zf:
            try:
                _cfg, _members, warnings = validate_archive(zf)
            except ValueError as exc:
                jobs.update(job_id, status="error", error=f"validation failed: {exc}")
                return

            members = [m for m in zf.infolist() if not m.is_dir()]
            jobs.update(job_id, total=len(members), message="Распаковка", phase="unpack")
            os.makedirs(dest, exist_ok=True)
            done = last = 0
            for m in members:
                zf.extract(m, dest)
                done += 1
                if done - last >= 25:
                    jobs.update(job_id, processed=done)
                    last = done

        # Statistics are computed once here and cached; shown as a plain status
        # (no progress bar) since it is a single pass.
        jobs.update(job_id, processed=len(members),
                    message="Подсчёт статистики", phase="stats")
        stats = compute_and_cache_stats(dest)
        stats["warnings"] = warnings
        jobs.update(job_id, status="done", result=stats)
    except Exception as exc:  # noqa: BLE001
        shutil.rmtree(dest, ignore_errors=True)
        jobs.update(job_id, status="error", error=str(exc))
    finally:
        try:
            os.remove(tmp_path)
        except OSError:
            pass


@app.post("/api/datasets")
def upload_dataset():
    if "file" not in request.files:
        return jsonify({"error": "no file part"}), 400
    file = request.files["file"]
    if not file.filename:
        return jsonify({"error": "empty filename"}), 400
    if not file.filename.lower().endswith(".zip"):
        return jsonify({"error": "only .zip archives are supported"}), 400

    name = safe_name(request.form.get("name") or file.filename)
    dest = os.path.join(UPLOADED_DIR, name)
    if os.path.exists(dest):
        return jsonify({"error": f"dataset '{name}' already exists"}), 409

    # Stream the upload to a temp file on disk instead of reading it all into
    # memory (a multi-GB archive in RAM would OOM-kill the worker). The transfer
    # has finished by the time we get here; unpacking runs in the background so
    # the client can poll extraction progress.
    tmp_dir = os.path.join(DATA_DIR, "_tmp")
    os.makedirs(tmp_dir, exist_ok=True)
    fd, tmp_path = tempfile.mkstemp(suffix=".zip", dir=tmp_dir)
    os.close(fd)
    file.save(tmp_path)

    job_id = jobs.create("upload", message="Подготовка")
    threading.Thread(
        target=_run_upload_job, args=(job_id, tmp_path, dest), daemon=True
    ).start()
    return jsonify({"job_id": job_id}), 202


@app.delete("/api/datasets/<kind>/<name>")
def delete_dataset(kind, name):
    root = kind_dir(kind)
    if root is None:
        return jsonify({"error": "unknown kind"}), 404
    name = safe_name(name)
    path = os.path.join(root, name)
    if not os.path.isdir(path):
        return jsonify({"error": "dataset not found"}), 404
    shutil.rmtree(path, ignore_errors=True)
    if kind == "augmented":
        meta = load_aug_meta()
        if name in meta:
            del meta[name]
            _save_json(AUG_META_FILE, meta)
    return jsonify({"ok": True})


def _run_augment_job(job_id, source_dir, dest, folder, source, display_name,
                     snapshots, scope):
    """Generate an augmented dataset in the background, reporting progress."""
    try:
        state = {"t": 0.0}

        def progress(done, total):
            now = time.time()
            if now - state["t"] >= 0.3 or done >= total:
                jobs.update(job_id, processed=done, total=total)
                state["t"] = now

        total = aug.count_images(source_dir, scope) * max(1, len(snapshots))
        jobs.update(job_id, total=total, message="Генерация аугментаций",
                    phase="generate")

        written = aug.generate(source_dir, dest, snapshots, scope=scope,
                               progress=progress)

        meta = load_aug_meta()
        meta[folder] = {
            "display_name": display_name,
            "source": source,
            "configs": snapshots,
            "scope": scope,
            "created_at": time.time(),
            "images": written,
        }
        _save_json(AUG_META_FILE, meta)

        jobs.update(job_id, message="Подсчёт статистики", phase="stats")
        stats = compute_stats(dest)
        stats["meta"] = meta[folder]
        try:
            _save_json(_stats_cache_path(dest), stats)
        except OSError:
            pass
        jobs.update(job_id, status="done", result=stats)
    except Exception as exc:  # noqa: BLE001
        shutil.rmtree(dest, ignore_errors=True)
        jobs.update(job_id, status="error", error=f"augmentation failed: {exc}")


@app.post("/api/datasets/augmented")
def create_augmented():
    if not aug.is_available():
        return jsonify({"error": "albumentations недоступна на сервере"}), 503
    body = request.get_json(silent=True) or {}
    source = safe_name(body.get("source"))
    display_name = (body.get("display_name") or "").strip()
    scope = body.get("scope", "all")
    if scope not in ("all", "train"):
        scope = "all"

    # accept a list of config ids; fall back to single config_id for compatibility
    config_ids = body.get("config_ids")
    if not config_ids and body.get("config_id"):
        config_ids = [body["config_id"]]
    if not config_ids:
        return jsonify({"error": "выберите хотя бы одну конфигурацию"}), 400

    source_dir = os.path.join(UPLOADED_DIR, source)
    if not os.path.isdir(source_dir):
        return jsonify({"error": "source dataset not found"}), 404

    all_configs = {c["id"]: c for c in load_configs()}
    # Snapshot the actual values so the record survives later config deletion.
    snapshots = []
    for cid in config_ids:
        cfg = all_configs.get(cid)
        if cfg is None:
            return jsonify({"error": f"config '{cid}' not found"}), 404
        snapshots.append({
            "config_id": cid,
            "name": cfg["name"],
            "transforms": cfg.get("transforms", []),
        })

    if not display_name:
        names = ", ".join(s["name"] for s in snapshots)
        display_name = f"{source} · {names}"

    folder = safe_name(display_name)
    dest = os.path.join(AUGMENTED_DIR, folder)
    suffix = 1
    while os.path.exists(dest):
        suffix += 1
        folder = f"{safe_name(display_name)}_{suffix}"
        dest = os.path.join(AUGMENTED_DIR, folder)
    os.makedirs(dest, exist_ok=True)

    job_id = jobs.create("augment", message="Подготовка")
    threading.Thread(
        target=_run_augment_job,
        args=(job_id, source_dir, dest, folder, source, display_name, snapshots, scope),
        daemon=True,
    ).start()
    return jsonify({"job_id": job_id}), 202


