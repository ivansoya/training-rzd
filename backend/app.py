RUNNING_STATES = {"preparing", "running"}


@app.get("/api/trainings/<run_id>/stream")
def stream_training(run_id):
    """Server-Sent Events: push live run state (epochs + per-batch progress) as
    soon as the training subprocess writes it, so the client never polls."""
    if read_state(run_id) is None:
        return jsonify({"error": "training not found"}), 404

    def gen():
        last = None
        idle = 0
        yield "retry: 2000\n\n"
        while True:
            state = read_state(run_id)
            if state is None:
                break
            payload = json.dumps(state, ensure_ascii=False)
            if payload != last:
                last = payload
                idle = 0
                yield "event: state\ndata: " + payload + "\n\n"
            else:
                idle += 1
                if idle >= 20:  # ~10s without change: keep proxies warm
                    idle = 0
                    yield ": keepalive\n\n"
            if state.get("status") not in RUNNING_STATES:
                yield "event: end\ndata: {}\n\n"
                break
            time.sleep(0.5)

    return Response(gen(), mimetype="text/event-stream", headers={
        "Cache-Control": "no-cache",
        "X-Accel-Buffering": "no",      # disable nginx response buffering
        "Connection": "keep-alive",
    })


